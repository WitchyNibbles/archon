#!/usr/bin/env node
// Generate/refresh the ESLint `max-lines` ratchet table for every src/**/*.ts file.
//
// WHAT THIS DOES
//   Scans src/**/*.ts (excluding .d.ts), counts lines, rounds each up to the
//   nearest 25 (with one line of guaranteed headroom so an ESLint/wc off-by-one
//   at an exact boundary can never cause a spurious failure), and writes the
//   result to eslint-max-lines-ratchet.mjs. eslint.config.js consumes that table
//   and enforces a per-file `max-lines` error, plus a default 800-line ceiling
//   (the house rule) for any NEW file not yet in the table.
//
// RATCHET POLICY — numbers may only DECREASE
//   The ceiling for a file is a one-way ratchet: it may go DOWN, never UP.
//   - When you legitimately SHRINK a file (e.g. extract a module), re-run this
//     script so its entry drops to the new, lower ceiling.
//   - You may NOT raise an entry to let a file grow. Growth is the smell the
//     ratchet exists to catch. If a file must genuinely grow, that is a design
//     decision requiring a recorded justification in the PR — and even then the
//     correct move is almost always to SPLIT the file (keeping every piece under
//     800) rather than raise a ceiling.
//
// HOW TO UPDATE AFTER A LEGIT SPLIT
//   1. Do the split (new modules land under src/, each < 800 lines).
//   2. Run:  node scripts/generate-max-lines-ratchet.mjs
//   3. Commit the regenerated eslint-max-lines-ratchet.mjs alongside the split.
//      The shrunk parent's entry drops; new modules get their own frozen entries.
//   4. `npm run lint` must pass with 0 warnings on the regenerated table.
//
// Rounding: ceil((lineCount + 1) / 25) * 25.

import { readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const srcRoot = path.join(repoRoot, "src");
const outputPath = path.join(repoRoot, "eslint-max-lines-ratchet.mjs");
const BUCKET = 25;

async function collectTsFiles(dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...(await collectTsFiles(full)));
    } else if (entry.isFile() && entry.name.endsWith(".ts") && !entry.name.endsWith(".d.ts")) {
      files.push(full);
    }
  }
  return files;
}

function countLines(content) {
  if (content.length === 0) {
    return 0;
  }
  const parts = content.split("\n");
  // A trailing newline yields a final empty element — that's wc -l semantics
  // (count of complete lines), not an extra line.
  return parts[parts.length - 1] === "" ? parts.length - 1 : parts.length;
}

function ceilingFor(lineCount) {
  // +1 guarantees at least one line of headroom before rounding, so an off-by-one
  // between wc -l and ESLint's line count at an exact 25-boundary is absorbed.
  return Math.ceil((lineCount + 1) / BUCKET) * BUCKET;
}

async function main() {
  const files = (await collectTsFiles(srcRoot)).sort();
  const table = {};
  for (const file of files) {
    const rel = path.relative(repoRoot, file).split(path.sep).join("/");
    const content = await readFile(file, "utf8");
    table[rel] = ceilingFor(countLines(content));
  }

  const body =
    `// GENERATED by scripts/generate-max-lines-ratchet.mjs — do not edit by hand.\n` +
    `// Per-file max-lines ratchet for src/**/*.ts. Numbers may only DECREASE:\n` +
    `// shrink a file, then re-run the generator to lower its entry; never raise an\n` +
    `// entry to let a file grow (record a justification and split the file instead).\n` +
    `// See scripts/generate-max-lines-ratchet.mjs for the full policy + update steps.\n` +
    `export default ${JSON.stringify(table, null, 2)};\n`;

  await writeFile(outputPath, body, "utf8");
  process.stdout.write(`wrote ${Object.keys(table).length} entries to ${path.relative(repoRoot, outputPath)}\n`);
}

main().catch((error) => {
  process.stderr.write(`${error instanceof Error ? error.stack ?? error.message : String(error)}\n`);
  process.exitCode = 1;
});
